    import pathlib
    out_dir = pathlib.Path("image_orient_tool/i18n")
    out_dir.mkdir(parents=True, exist_ok=True)
    for lang in LANG_NUMERIC:
        ts_content = build_ts(lang)
        out_path = out_dir / f"app_{lang}.ts"
        out_path.write_text(ts_content, encoding="utf-8")
        print(f"wrote {out_path} ({len(ts_content)} bytes)")
