LANG_NUMERIC = {
    "en": "en_US", "ko": "ko_KR", "de": "de_DE", "ru": "ru_RU", "fr": "fr_FR",
    "zh_CN": "zh_CN", "zh_TW": "zh_TW",
}



def build_ts(lang: str) -> str:
    ts = ET.Element("TS", version="2.1", language=LANG_NUMERIC[lang])
    for context_name, entries in DATA.items():
        context_el = ET.SubElement(ts, "context")
        ET.SubElement(context_el, "name").text = context_name
        for source, translations in entries:
            message_el = ET.SubElement(context_el, "message")
            ET.SubElement(message_el, "source").text = source
            ET.SubElement(message_el, "translation").text = translations[lang]
    rough = ET.tostring(ts, encoding="unicode")
    pretty = minidom.parseString(rough).toprettyxml(indent="    ")
    # DOCTYPE行を付与(Qt Linguist形式)
    lines = pretty.split("\n")
    lines = [l for l in lines if l.strip()]
    lines.insert(1, "<!DOCTYPE TS>")
    return "\n".join(lines) + "\n"


